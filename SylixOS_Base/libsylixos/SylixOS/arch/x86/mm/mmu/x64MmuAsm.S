#ifndef ASSEMBLY
#define ASSEMBLY 1
#endif

#include <arch/assembler.h>
#include <config/kernel/vmm_cfg.h>

#if LW_CFG_VMM_EN > 0

    FILE_BEGIN()

    EXPORT_LABEL(x64MmuEnable)
    EXPORT_LABEL(x64MmuDisable)
    EXPORT_LABEL(x64MmuInvalidateTLB)
    EXPORT_LABEL(x86MmuInvalidateTLB)
    EXPORT_LABEL(x64MmuInvalidateTLBMVA)

;/*********************************************************************************************************
;  使能 MMU
;*********************************************************************************************************/

FUNC_DEF(x64MmuEnable)
    MOV     %CR0 , %RAX
    MOV     $(X86_CR0_PG | X86_CR0_WP) , %RCX
    OR      %RCX , %RAX                                                 ;/*  设置 PG 和 WP               */
    MOV     %RAX , %CR0
    JMP     __x64MmuEnable                                              ;/*  排干预取队列                */
LINE_LABEL(__x64MmuEnable)
    RET
    FUNC_END(x64MmuEnable)

;/*********************************************************************************************************
;  禁能 MMU
;  注意: long 模式不能关闭 MMU
;*********************************************************************************************************/

FUNC_DEF(x64MmuDisable)
    RET
    FUNC_END(x64MmuDisable)

;/*********************************************************************************************************
;  无效所有 TLB
;*********************************************************************************************************/

FUNC_DEF(x64MmuInvalidateTLB)
FUNC_DEF(x86MmuInvalidateTLB)                                           ;/*  向前兼容接口                */
    MOV     %CR3 , %RAX
    MOV     %RAX , %CR3
    JMP     __x64MmuInvalidateTLB                                       ;/*  排干预取队列                */
LINE_LABEL(__x64MmuInvalidateTLB)
    RET
    FUNC_END(x64MmuInvalidateTLB)

;/*********************************************************************************************************
;  无效指定 MVA 的 TLB
;*********************************************************************************************************/

FUNC_DEF(x64MmuInvalidateTLBMVA)
    INVLPG  (X86_64_ARG0)                                               ;/*  无效 TLB 条目               */
    JMP     __x64MmuInvalidateTLBMVA                                    ;/*  排干预取队列                */
LINE_LABEL(__x64MmuInvalidateTLBMVA)
    RET
    FUNC_END(x64MmuInvalidateTLBMVA)

    FILE_END()

#endif
