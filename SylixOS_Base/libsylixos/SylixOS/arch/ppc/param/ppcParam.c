#define  __SYLIXOS_KERNEL
#include "SylixOS.h"
#include "ppcParam.h"
/*********************************************************************************************************
  启动参数
*********************************************************************************************************/
static PPC_PARAM    ppcParam = { LW_TRUE, LW_TRUE };
/*********************************************************************************************************
** 函数名称: archKernelParam
** 功能描述: PowerPC 体系构架启动参数设置.
** 输　入  : pcParam       启动参数
**                         ppc32=yes        是否为通用的 PowerPC 32-Bit 处理器
** 输　出  : NONE
** 全局变量:
** 调用模块:
*********************************************************************************************************/
VOID  archKernelParam (CPCHAR  pcParam)
{
    if (lib_strncmp(pcParam, "ppc32=", 6) == 0) {                       /*  是否为通用的 PPC32 处理器   */
        if (pcParam[6] == 'n') {
            ppcParam.PP_bGenericPPC32 = LW_FALSE;                       /*  E500 PPC460                 */
        } else {
            ppcParam.PP_bGenericPPC32 = LW_TRUE;                        /*  E300 PPC750                 */
        }

    } else if (lib_strncmp(pcParam, "unalign=", 8) == 0) {              /*  是否支持非对齐访问          */
        if (pcParam[8] == 'n') {
            ppcParam.PP_bUnalign = LW_FALSE;
        } else {
            ppcParam.PP_bUnalign = LW_TRUE;
        }
    }
}
/*********************************************************************************************************
** 函数名称: archKernelParamGet
** 功能描述: 获取启动参数.
** 输　入  : NONE
** 输　出  : 参数
** 全局变量:
** 调用模块:
*********************************************************************************************************/
PPC_PARAM  *archKernelParamGet (VOID)
{
    return  (&ppcParam);
}
