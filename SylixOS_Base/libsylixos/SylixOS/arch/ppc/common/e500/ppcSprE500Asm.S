MACRO_DEF(SET_SPR, spr)
    MTSPR       \spr , R3
    ISYNC
    BLR
    MACRO_END()

MACRO_DEF(GET_SPR, spr)
    MFSPR       R3 , \spr
    ISYNC
    BLR
    MACRO_END()

;/*********************************************************************************************************
;  读取 DEAR 寄存器
;*********************************************************************************************************/

FUNC_DEF(ppcE500GetDEAR)
