#define ASSEMBLY 1
#endif

#include <arch/assembler.h>

    FILE_BEGIN()

#if LW_CFG_SMP_EN > 0

    IMPORT_LABEL(bspMpInt)

    EXPORT_LABEL(archMpCur)
    EXPORT_LABEL(archMpInt)

    WEAK(archMpCur)
    WEAK(archMpInt)

;/*********************************************************************************************************
;  获得当前核 ID
;*********************************************************************************************************/

FUNC_DEF(archMpCur)
    MOVI        A0 , 0
    RTS
    FUNC_END(archMpCur)

;/*********************************************************************************************************
;  产生一次核间中断
;*********************************************************************************************************/

FUNC_DEF(archMpInt)
    JMPI        bspMpInt
    FUNC_END(archMpInt)

    FILE_END()

#endif
