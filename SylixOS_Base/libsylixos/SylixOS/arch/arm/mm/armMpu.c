#if (LW_CFG_VMM_EN == 0) && (LW_CFG_ARM_MPU > 0)
#include "mpu/v7m/armMpuV7M.h"
#include "mpu/v7r/armMpuV7R.h"
/*********************************************************************************************************
** 函数名称: archMpuInit
** 功能描述: 初始化 MPU
** 输　入  : pcMachineName  机器名称
**           pmpuregion     内存分区信息
** 输　出  : NONE
** 全局变量:
** 调用模块:
*********************************************************************************************************/
VOID  archMpuInit (CPCHAR  pcMachineName, const ARM_MPU_REGION  mpuregion[])
{
#if defined(__SYLIXOS_ARM_ARCH_M__)
    armMpuV7MInit(pcMachineName, mpuregion);

#elif defined(__SYLIXOS_ARM_ARCH_R__)
    armMpuV7RInit(pcMachineName, mpuregion);
#endif                                                                  /*  __SYLIXOS_ARM_ARCH_R__      */
}

#endif                                                                  /*  LW_CFG_ARM_MPU > 0          */
