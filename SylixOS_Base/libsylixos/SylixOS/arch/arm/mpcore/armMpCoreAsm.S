#ifndef ASSEMBLY
#define ASSEMBLY 1
#endif

#include <arch/assembler.h>

#if LW_CFG_SMP_EN > 0

    FILE_BEGIN()
    
    IMPORT_LABEL(bspMpInt)
    
    EXPORT_LABEL(archMpCur)
    EXPORT_LABEL(archMpInt)
    
    WEAK(archMpCur)
    WEAK(archMpInt)

;/*********************************************************************************************************
;  获得当前核 ID ARMv6/v7
;*********************************************************************************************************/

#if __SYLIXOS_ARM_ARCH__ >= 6
FUNC_DEF(archMpCur)
    MRC     p15, 0, R0, c0, c0, 5
    AND     R0, R0, #0x03
    BX      LR
    FUNC_END()

;/*********************************************************************************************************
;  获得当前核 ID ARMv4/v5
;*********************************************************************************************************/

#else
FUNC_DEF(archMpCur)
    MOV     R0, #0
    BX      LR
    FUNC_END()
#endif
    
;/*********************************************************************************************************
;  产生一次核间中断
;*********************************************************************************************************/

FUNC_DEF(archMpInt)
    B       bspMpInt
    FUNC_END()
    
    FILE_END()

#endif
;/*********************************************************************************************************
;  END
