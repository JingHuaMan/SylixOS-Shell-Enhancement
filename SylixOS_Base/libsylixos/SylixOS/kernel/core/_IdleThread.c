#define  __SYLIXOS_KERNEL
#include "../SylixOS/kernel/include/k_kernel.h"
/*********************************************************************************************************
** 函数名称: _IdleThread
** 功能描述: 空闲线程
** 输　入  : 
** 输　出  : 
** 全局变量: 
** 调用模块: 
*********************************************************************************************************/
PVOID  _IdleThread (PVOID  pvArg)
{
    ULONG  ulCPUId = (ULONG)pvArg;
    
    for (;;) {
        bspTaskIdleHook();                                              /*  空闲任务钩子函数            */
        __LW_THREAD_IDLE_HOOK(ulCPUId);
    }
    
    return  (LW_NULL);
}
