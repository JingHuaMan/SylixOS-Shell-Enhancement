/*********************************************************************************************************
** 函数名称: _StackAllocate
** 功能描述: 分配栈内存
** 输　入  : ptcb      任务
**           ulOption  线程创建选项
**           stSize    需要的栈大小
** 输　出  : 栈内存
** 全局变量:
** 调用模块:
*********************************************************************************************************/
PLW_STACK  _StackAllocate (PLW_CLASS_TCB  ptcb, ULONG  ulOption, size_t  stSize)
{
#if LW_CFG_MODULELOADER_EN > 0
    return  ((PLW_STACK)vprocStackAlloc(ptcb, ulOption, stSize));
#else
    return  ((PLW_STACK)__KHEAP_ALLOC(stSize));
#endif                                                                  /*  LW_CFG_MODULELOADER_EN > 0  */
}
/*********************************************************************************************************
** 函数名称: _StackFree
** 功能描述: 回收栈内存
** 输　入  : ptcbDel       任务
**           pstk          栈内存
** 输　出  : NONE
** 全局变量:
** 调用模块:
*********************************************************************************************************/
VOID  _StackFree (PLW_CLASS_TCB  ptcb, PLW_STACK  pstk)
{
#if LW_CFG_MODULELOADER_EN > 0
    vprocStackFree(ptcb, (PVOID)pstk);
#else
    __KHEAP_FREE((PVOID)pstk);
#endif                                                                  /*  LW_CFG_MODULELOADER_EN > 0  */
}
