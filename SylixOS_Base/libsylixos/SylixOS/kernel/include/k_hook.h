#ifndef __K_HOOK_H
#define __K_HOOK_H

/*********************************************************************************************************
  线程创建 HOOK
  此 HOOK 在任务上下文中被调用.
*********************************************************************************************************/
#define LW_OPTION_THREAD_CREATE_HOOK        1

/*********************************************************************************************************
  线程删除 HOOK
  此 HOOK 在任务上下文中被调用.
*********************************************************************************************************/
#define LW_OPTION_THREAD_DELETE_HOOK        2

/*********************************************************************************************************
  线程切换 HOOK
  此 HOOK 在任务切换间隙被调用, 并且操作系统调用此函数时处于锁定内核并关中断状态!
*********************************************************************************************************/
#define LW_OPTION_THREAD_SWAP_HOOK          3

/*********************************************************************************************************
  时钟中断 HOOK
  此 HOOK 在中断上下文中且在锁定内核后被调用.
*********************************************************************************************************/
#define LW_OPTION_THREAD_TICK_HOOK          4

/*********************************************************************************************************
  线程初始化过程 HOOK
  此 HOOK 在任务上下文中被调用.
*********************************************************************************************************/
#define LW_OPTION_THREAD_INIT_HOOK          5

/*********************************************************************************************************
  空闲线程 HOOK
  此 HOOK 在任务上下文中被调用, 空闲线程会不间断的调用此 HOOK, 注意, 此 HOOK 只能在系统进入多任务前被设置.
*********************************************************************************************************/
#define LW_OPTION_THREAD_IDLE_HOOK          6

/*********************************************************************************************************
  系统初始化 HOOK
  此 HOOK 被淘汰.
*********************************************************************************************************/
#define LW_OPTION_KERNEL_INITBEGIN          7

/*********************************************************************************************************
  系统初始化完成 HOOK
  此 HOOK 在系统还没有进入多任务状态时被调用.
*********************************************************************************************************/
#define LW_OPTION_KERNEL_INITEND            8

/*********************************************************************************************************
  系统重启(关机) HOOK
  此 HOOK 在任务上下文中被调用.
*********************************************************************************************************/
#define LW_OPTION_KERNEL_REBOOT             9

/*********************************************************************************************************
  系统软件看门狗 HOOK
  此 HOOK 在关中断且进入内核状态下被调用.
*********************************************************************************************************/
#define LW_OPTION_WATCHDOG_TIMER            10

/*********************************************************************************************************
  内核对象创建 HOOK
  此 HOOK 在任务上下文中被调用.
*********************************************************************************************************/
#define LW_OPTION_OBJECT_CREATE_HOOK        11

/*********************************************************************************************************
  内核对象删除 HOOK
  此 HOOK 在任务上下文中被调用.
*********************************************************************************************************/
#define LW_OPTION_OBJECT_DELETE_HOOK        12

/*********************************************************************************************************
  文件描述符创建 HOOK
  此 HOOK 在 I/O 系统锁定状态下被调用.
*********************************************************************************************************/
#define LW_OPTION_FD_CREATE_HOOK            13

/*********************************************************************************************************
  文件描述符删除 HOOK
  此 HOOK 在 I/O 系统锁定状态下被调用.
*********************************************************************************************************/
#define LW_OPTION_FD_DELETE_HOOK            14

/*********************************************************************************************************
  当前 CPU 准备运行/恢复运行 IDLE 任务 HOOK
  此 HOOK 在关中断且锁定内核时被调用.
*********************************************************************************************************/
#define LW_OPTION_CPU_IDLE_ENTER            15

/*********************************************************************************************************
  当前 CPU IDLE 任务被抢占 HOOK
  此 HOOK 在关中断且锁定内核时被调用.
*********************************************************************************************************/
#define LW_OPTION_CPU_IDLE_EXIT             16

/*********************************************************************************************************
  当前 CPU 发生中断, 在调用中断处理函数前 HOOK
  此 HOOK 在中断上下文中被调用.
*********************************************************************************************************/
#define LW_OPTION_CPU_INT_ENTER             17

/*********************************************************************************************************
  当前 CPU 发生中断, 在调用中断处理函数后 HOOK
  此 HOOK 在中断上下文中被调用.
*********************************************************************************************************/
#define LW_OPTION_CPU_INT_EXIT              18

/*********************************************************************************************************
  线程堆栈溢出 HOOK
  此 HOOK 可能发生在内核锁定状态下被调用.
*********************************************************************************************************/
#define LW_OPTION_STACK_OVERFLOW_HOOK       25

/*********************************************************************************************************
  线程出现致命错误 (接收到异常信号) HOOK
  此 HOOK 在任务上下文中被调用.
*********************************************************************************************************/
#define LW_OPTION_FATAL_ERROR_HOOK          26

/*********************************************************************************************************
  进程创建 HOOK
  此 HOOK 在任务上下文中被调用.
*********************************************************************************************************/
#define LW_OPTION_VPROC_CREATE_HOOK         30

/*********************************************************************************************************
  进程删除 HOOK
  此 HOOK 在任务上下文中被调用.
*********************************************************************************************************/
#define LW_OPTION_VPROC_DELETE_HOOK         31

#endif                                                                  /*  __K_HOOK_H                  */
/*********************************************************************************************************
  END
*********************************************************************************************************/
