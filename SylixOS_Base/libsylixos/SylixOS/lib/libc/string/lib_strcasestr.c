/*********************************************************************************************************
** 函数名称: lib_strcasestr
** 功能描述: 
** 输　入  : 
** 输　出  : 
** 全局变量: 
** 调用模块: 
*********************************************************************************************************/
PCHAR  lib_strcasestr (CPCHAR  cpcS1, CPCHAR  cpcS2)
{
    CHAR   c, sc;
    size_t len;

    if ((c = *cpcS2++) != 0) {
        c = lib_tolower((unsigned char)c);
        len = lib_strlen(cpcS2);
        do {
            do {
                if ((sc = *cpcS1++) == 0) {
                    return  (LW_NULL);
                }
            } while ((char)lib_tolower((unsigned char)sc) != c);
        } while (lib_strncasecmp(cpcS1, cpcS2, len) != 0);
        cpcS1--;
    }
    
    return  ((char *)cpcS1);
}
