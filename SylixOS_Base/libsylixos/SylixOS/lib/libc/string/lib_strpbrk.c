/*********************************************************************************************************
** 函数名称: lib_strpbrk
** 功能描述: 
** 输　入  : 
** 输　出  : 
** 全局变量: 
** 调用模块: 
*********************************************************************************************************/
PCHAR  lib_strpbrk (CPCHAR  cpcS1, CPCHAR  cpcS2)
{
    const char *scanp;
    int         c, sc;

    while ((c = *cpcS1++) != 0) {
        for (scanp = cpcS2; (sc = *scanp++) != 0;) {
            if (sc == c) {
                return ((char *)(cpcS1 - 1));
            }
        }
    }

    return  (LW_NULL);
}
