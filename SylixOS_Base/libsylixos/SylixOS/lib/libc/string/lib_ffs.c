#define  __SYLIXOS_KERNEL
#include "../SylixOS/kernel/include/k_kernel.h"
/*********************************************************************************************************
** 函数名称: lib_ffs
** 功能描述:  
** 输　入  : 
** 输　出  : 
** 全局变量: 
** 调用模块: 
*********************************************************************************************************/
int  lib_ffs (int  valu)
{    
    return  (archFindLsb((UINT32)valu));
}
/*********************************************************************************************************
** 函数名称: lib_ffsll
** 功能描述:  
** 输　入  : 
** 输　出  : 
** 全局变量: 
** 调用模块: 
*********************************************************************************************************/
int  lib_ffsll (long long int  valu)
{
    unsigned long long int  x = valu & -valu;
    
    if (x <= 0xffffffff) {
        return  (lib_ffs((int)valu));
    } else {
        return  (32 + lib_ffs((int)(valu >> 32)));
    }
}
