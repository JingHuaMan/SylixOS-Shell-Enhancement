#ifndef __LIB_ASSERT_H
#define __LIB_ASSERT_H

/*********************************************************************************************************
  根据 NDEBUG 选择是否执行 arch assert 函数
*********************************************************************************************************/

#ifdef NDEBUG
#define lib_assert(condition)   \
        ((void)0)
#else
#define lib_assert(condition)   \
        (void)((condition) || (archAssert(#condition, __func__, __FILE__, __LINE__), 0))
#endif

#endif                                                                  /*  __LIB_ASSERT_H              */
