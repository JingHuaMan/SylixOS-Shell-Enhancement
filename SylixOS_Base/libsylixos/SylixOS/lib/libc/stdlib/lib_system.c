#define  __SYLIXOS_KERNEL
#include "unistd.h"
/*********************************************************************************************************
** 函数名称: lib_abort
** 功能描述: 
** 输　入  : 
** 输　出  : 
** 全局变量: 
** 调用模块: 
*********************************************************************************************************/
VOID  lib_abort (VOID)
{
    LW_OBJECT_HANDLE  ulMe;

#if LW_CFG_SIGNAL_EN > 0
    pid_t  pidMe;

    pidMe = getpid();
    if (pidMe > 0) {
        kill(pidMe, SIGABRT);
    } else {
        raise(SIGABRT);
    }

    sleep(1);
    signal(SIGABRT, SIG_DFL);

    if (pidMe > 0) {
        kill(pidMe, SIGABRT);
    } else {
        raise(SIGABRT);
    }
#endif                                                                  /*  LW_CFG_SIGNAL_EN > 0        */
    
    ulMe = API_ThreadIdSelf();
    API_ThreadForceDelete(&ulMe, (PVOID)EXIT_FAILURE);
}
/*********************************************************************************************************
** 函数名称: lib_system
** 功能描述: 
** 输　入  : 
** 输　出  : 
** 全局变量: 
** 调用模块: 
*********************************************************************************************************/
INT  lib_system (CPCHAR  pcCmd)
{
#if LW_CFG_SHELL_EN > 0
    INT     iRet;
    
    if (!pcCmd) {
        _ErrorHandle(EINVAL);
        return  (PX_ERROR);
    }
    
    __THREAD_CANCEL_POINT();                                            /*  测试取消点                  */
    
    iRet = API_TShellExec(pcCmd);

    return  (iRet);
#else
    return  (PX_ERROR);
#endif                                                                  /*  LW_CFG_SHELL_EN > 0         */
}
