/*********************************************************************************************************
** 函数名称: lib_div
** 功能描述: 
** 输　入  : 
** 输　出  : 
** 全局变量: 
** 调用模块: 
*********************************************************************************************************/
lib_div_t       lib_div(int  numer, int  denom)
{
    lib_div_t result;

    result.quot = numer / denom;
    result.rem = numer % denom;
    
    if (numer >= 0 && result.rem < 0) {
        result.quot++;
        result.rem -= denom;
    }
    
    return  (result);
}
