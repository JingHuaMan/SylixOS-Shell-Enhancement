#ifndef __FS_INTERNAL_H
#define __FS_INTERNAL_H

/*********************************************************************************************************
  磁盘操作宏
*********************************************************************************************************/

#define	BLK_LD_WORD(ptr)        (UINT16)(((UINT16)*((BYTE*)(ptr) + 1) <<  8) |  \
                                          (UINT16)*(BYTE*)(ptr))
#define	BLK_LD_DWORD(ptr)		(UINT32)(((UINT32)*((BYTE*)(ptr) + 3) << 24) |  \
                                         ((UINT32)*((BYTE*)(ptr) + 2) << 16) |  \
                                         ((UINT32)*((BYTE*)(ptr) + 1) <<  8) |  \
                                          (UINT32)*(BYTE*)(ptr))
                                          
#define	BLK_ST_WORD(ptr,val)	*(BYTE*)(ptr)       = (BYTE)(val);    \
                                *((BYTE*)(ptr) + 1) = (BYTE)((UINT16)(val) >> 8)
#define	BLK_ST_DWORD(ptr,val)	*(BYTE*)(ptr)       = (BYTE)(val);                  \
                                *((BYTE*)(ptr) + 1) = (BYTE)((UINT16)(val) >>  8);  \
                                *((BYTE*)(ptr) + 2) = (BYTE)((UINT32)(val) >> 16);  \
                                *((BYTE*)(ptr) + 3) = (BYTE)((UINT32)(val) >> 24)

#endif                                                                  /*  __FS_INTERNAL_H             */
